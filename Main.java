    private void add_to_txtfields(int row){
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        
        tf_st_id.setText(model.getValueAt(row, 1).toString());
        tf_fname.setText(model.getValueAt(row, 2).toString());
        tf_lname.setText(model.getValueAt(row, 3).toString());
        tf_add.setText(model.getValueAt(row, 4).toString());
        gender.setSelectedItem(model.getValueAt(row, 5));
        tf_ph.setText(model.getValueAt(row, 6).toString());
        math1.setSelectedItem(model.getValueAt(row, 7));
        database.setSelectedItem(model.getValueAt(row, 8));
        Science.setSelectedItem(model.getValueAt(row, 10));
        game.setSelectedItem(model.getValueAt(row, 9));
    }
    private void tableKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tableKeyPressed
        // TODO add your handling code here:
        int row = table.getSelectedRow();
        int key_code = evt.getKeyCode();
        
        if (key_code==38){
            if (row!=0)
                row -= 1;}
        
        else if(key_code==40){
            if (row!=table.getRowCount()-1)
                row += 1;}
        
        add_to_txtfields(row);
    }//GEN-LAST:event_tableKeyPressed

