    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
 
        try {
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            int row = table.getSelectedRow();
            int st_id = Integer.parseInt(model.getValueAt(row, 1).toString());
            
            PreparedStatement pst = sqlConn.prepareCall("DELETE FROM student WHERE st_id = ?");
            pst.setInt(1, st_id);
            pst.execute();
            pst.close();
            
            JOptionPane.showMessageDialog(rootPane, "Deleted!", "Info", 1);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "???????? :)", "Error", 1);
        }
        updateTable();
        reset_fields();
    }//GEN-LAST:event_btn_deleteActionPerformed

