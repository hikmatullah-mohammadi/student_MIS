  // update the table
    private void updateTable(){
        try {;
            Statement stmt = sqlConn.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT * FROM student;");
            
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            
            // delete all rows
            try{
                do{
                model.removeRow(0);
                }while(model.getRowCount()!=0);
            } catch(Exception e){
            }
            
            // add new records from the table
            while (rs.next()){
                int st_id = rs.getInt("st_id");
                String fname = rs.getString("fname");
                String lname = rs.getString("lname");
                String gender = rs.getString("gender");
                String address = rs.getString("address");
                String ph_num = rs.getString("ph_number");
                String math = rs.getString("math");
                String scinece = rs.getString("science");
                String game = rs.getString("game");
                String dbase = rs.getString("dbase");

                Object[] record = {model.getRowCount()+1, st_id, fname, lname, address, gender, ph_num, math, scinece, game, dbase};
                
                // add records to the table
                model.addRow(record);
            }
            
        } catch (Exception e) {
            System.out.println(e);
        }
    }
    
